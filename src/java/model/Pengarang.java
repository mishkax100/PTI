package model;

/**
 *
 * @author Bona
 */
public class Pengarang {
    private String id_pengarang;
    private String nama;
    private String jenis_kelamin;
    private String password;
    private String email;
    private String status;
    private String h_index;
    private String no_telp;
    private String TTL;
    private String prodi;
    private String alamat;

    public String getId_pengarang() {
        return id_pengarang;
    }

    public void setId_pengarang(String id_pengarang) {
        this.id_pengarang = id_pengarang;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getJenis_kelamin() {
        return jenis_kelamin;
    }

    public void setJenis_kelamin(String jenis_kelamin) {
        this.jenis_kelamin = jenis_kelamin;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getH_index() {
        return h_index;
    }

    public void setH_index(String h_index) {
        this.h_index = h_index;
    }

    public String getNo_telp() {
        return no_telp;
    }

    public void setNo_telp(String no_telp) {
        this.no_telp = no_telp;
    }

    public String getTTL() {
        return TTL;
    }

    public void setTTL(String TTL) {
        this.TTL = TTL;
    }

    public String getProdi() {
        return prodi;
    }

    public void setProdi(String prodi) {
        this.prodi = prodi;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }
    
}
